#!/usr/bin/env python
"""Run the GridTrace evaluation suite against an eval JSON file.

Default config uses the in-memory backend with the
``HashingEmbedder`` (no network), so the script works fully offline.
"""

from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parents[1]))

from gridtrace.embedder import HashingEmbedder
from gridtrace.eval import EvalEngine, compute_metrics
from gridtrace.loaders import load_entries
from gridtrace.utils.config import GridTraceConfig
from gridtrace.utils.logging import configure_logging, get_logger

logger = get_logger(__name__)


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description="Run GridTrace evaluation suite.")
    parser.add_argument(
        "--eval", default="data/eval/eval_v3.json", help="Path to eval JSON"
    )
    parser.add_argument(
        "--faq", default="data/faq/OpsWarden_FAQ.md", help="Path to FAQ source"
    )
    parser.add_argument("--epsilon", type=float, default=0.05, help="Quantization epsilon")
    parser.add_argument("--anchor-top-k", type=int, default=8)
    parser.add_argument("--score-threshold", type=float, default=0.0)
    parser.add_argument("--top-k", type=int, default=3)
    parser.add_argument(
        "--output", default="docs/eval_report.md", help="Output markdown report"
    )
    args = parser.parse_args(argv)

    configure_logging()

    eval_path = Path(args.eval)
    faq_path = Path(args.faq)
    if not eval_path.exists():
        print(f"Eval file not found: {eval_path}", file=sys.stderr)
        return 1
    if not faq_path.exists():
        print(f"FAQ file not found: {faq_path}", file=sys.stderr)
        return 1

    eval_data = json.loads(eval_path.read_text(encoding="utf-8"))
    cfg = GridTraceConfig(
        storage_backend="memory",
        epsilon=args.epsilon,
        anchor_top_k=args.anchor_top_k,
        score_threshold=args.score_threshold,
        top_k=args.top_k,
        embedding_dim=512,
    )
    embedder = HashingEmbedder(dim=512)
    engine = EvalEngine(embedder, cfg)

    # ingest
    entries = load_entries(faq_path)
    print(f"Ingesting {len(entries)} FAQ entries from {faq_path.name}…")
    for e in entries:
        engine.ingest(
            question=e.question,
            solution=e.solution,
            category=e.category,
            doc_id=e.doc_id,
            page_index=e.page_index,
        )

    # predict
    from gridtrace.eval.engine import EvalPrediction

    predictions: list[EvalPrediction] = []
    for q in eval_data:
        pred = engine.predict(
            query=q["query"],
            expected_doc_id=q["expected_doc_id"],
            expected_page_index=q["expected_page_index"],
            top_k=args.top_k,
            anchor_top_k=args.anchor_top_k,
            score_threshold=args.score_threshold,
        )
        predictions.append(pred)

    metrics = compute_metrics(predictions)
    summary = metrics.as_dict()
    print(json.dumps(summary, ensure_ascii=False, indent=2))

    # write report
    report_path = Path(args.output)
    report_path.parent.mkdir(parents=True, exist_ok=True)
    lines = [
        "# GridTrace Evaluation Report",
        "",
        f"- Eval set: `{eval_path.name}` ({len(eval_data)} queries)",
        f"- FAQ: `{faq_path.name}` ({len(entries)} entries)",
        f"- Embedder: `HashingEmbedder(dim=512)`",
        f"- ε (epsilon): `{args.epsilon}`",
        f"- anchor_top_k: `{args.anchor_top_k}`",
        f"- score_threshold: `{args.score_threshold}`",
        f"- top_k: `{args.top_k}`",
        "",
        "## Aggregate metrics",
        "",
        "| metric | value |",
        "|---|---|",
    ]
    for k, v in summary.items():
        lines.append(f"| `{k}` | {v} |")
    lines += [
        "",
        "## Per-query details",
        "",
        "| query | expected | predicted (top-K) | L1 hit? |",
        "|---|---|---|---|",
    ]
    for p in predictions:
        l1 = "✅" if p.expected_doc_id in p.l1_anchor_doc_ids else "❌"
        pred = ", ".join(f"`{d}#{pi}`" for d, pi in zip(p.predicted_doc_ids, p.predicted_page_indexes)) or "—"
        lines.append(
            f"| {p.query} | `{p.expected_doc_id}#{p.expected_page_index}` | {pred} | {l1} |"
        )
    lines += ["", "_Generated by `scripts/run_eval.py`._"]
    report_path.write_text("\n".join(lines) + "\n", encoding="utf-8")
    print(f"Report written to {report_path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
